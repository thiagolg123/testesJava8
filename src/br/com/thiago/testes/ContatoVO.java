package br.com.thiago.testes;

public class ContatoVO {

	private String nome;
	private int idade;
	private double algumDouble;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public double getAlgumDouble() {
		return algumDouble;
	}

	public void setAlgumDouble(double algumDouble) {
		this.algumDouble = algumDouble;
	}

}
